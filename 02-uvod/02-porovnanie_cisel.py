a = int(input("Zadaj číslo a: "))
b = int(input("Zadaj číslo b: "))
if a > b:
    print("Väčšie je číslo", a)
else:
    print("Väčšie je číslo", b)
# if je podmieneny prikaz
# najskor zisti splnenie podmienky, ak plati, tak sa vykonaju prikazy za dvojbodkou (odsadene od kraja)
# ak podmienka neplatí, vykonajú sa príkazy za else: (odsadene od kraja)